# O que é um cabeçalho dentro de um arquivo Python?
#    É um bloco de comentários no início do código que contém informações
#    como autor, data, versão e descrição do script.
#    Ele serve apenas para documentação e organização do código,
#    não é interpretado pelo Python.

# Para que serve o cabeçalho?
#    Serve para informar dados gerais sobre o arquivo,
#    facilitar a manutenção, colaboração entre programadores
#    e dar contexto sobre o propósito do código.

# Qual a diferença do cabeçalho para a docstring?
#    Cabeçalho: escrito como comentários (#), ignorado pelo Python,
#    usado apenas para leitura humana.
#    Docstring: escrita entre """aspas triplas""", é interpretada
#    e armazenada em tempo de execução, podendo ser acessada
#    com help() ou __doc__.

# O que é uma docstring?
#    É uma string de documentação usada para descrever
#    módulos, classes, funções ou métodos.
#    Diferente do cabeçalho, pode ser acessada em tempo de execução,
#    ajudando na compreensão e manutenção do código.
